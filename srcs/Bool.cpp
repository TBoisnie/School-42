
#include "Bool.hpp"


// =============================================================================
// Public
// =============================================================================
// - Types ---------------------------------------------------------------------
// - Attributes ----------------------------------------------------------------
// - Constructors & Destructor & Assignment ------------------------------------
Bool::Bool(
	value_type value
)
	: _value{value}
{
	// Nothing
}

// - Operators -----------------------------------------------------------------
Bool::self_type
Bool::operator~() const
{
	return self_type{!this->_value};
}

Bool::self_type
Bool::operator&(
	self_const_ref_type other
) const
{
	return self_type{this->_value && other._value};
}

Bool::self_type
Bool::operator|(
	self_const_ref_type other
) const
{
	return self_type{this->_value || other._value};
}

// - Methods -------------------------------------------------------------------
// - Getters & Setters ---------------------------------------------------------
Bool::value_type
Bool::getValue() const noexcept
{
	return this->_value;
}

