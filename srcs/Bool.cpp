// - Constructors & Destructor & Assignment ------------------------------------
// - Operators -----------------------------------------------------------------
// - Methods -------------------------------------------------------------------
// - Getters & Setters ---------------------------------------------------------

// =============================================================================
// Overload
// =============================================================================
// - IO Stream -----------------------------------------------------------------
std::ostream &
operator<<(
	std::ostream & os,
	Bool::self_const_ref_type b
)
{
	os << b.getValue();

	return os;
}